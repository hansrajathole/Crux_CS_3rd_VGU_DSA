package Lec2;

public class Relational {
    public static void main(String[] args) {
        int x = 10;
        int y = 12;

        System.out.println(x>y);
        System.out.println(x>=y);
        System.out.println(x<y);
        System.out.println(x<=y);
        System.out.println(x == y);
        System.out.println(x!= y);
    }
}
