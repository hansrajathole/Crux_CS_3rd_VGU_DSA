package Lec8;

public class for_loop_Demo {
    public static void main(String[] args) {
        
        // First Way
        for(int i = 1;i<=10;i++){
            System.out.println(i);
        }

        // Second Way
        int i = 1;
        for(;i<=10;i++){
            System.out.println(i);
        }


          // Third Way
        int j = 1;
        for(;j<=10;){
            System.out.println(i);
            j++;
        }

           // four Way
        for(int k = 1;k<=10;){
            System.out.println(i);
            k++;
        }
    }
}
